package curso.condicionales;

import java.util.Scanner;

public class Condicionales7_AMGC {

	public static void main(String[] args) {
		/* 7. El director de una escuela está organizando un viaje de estudios y requiere determinar
		 * cuánto debe cobrar a cada alumno y cuánto debe pagar a la compañía de viajes por el servicio.
		 * La forma de cobrar es la siguiente:
		 * 
		 * * Si son 100 alumnos o más, el costo por cada alumno es de 65 euros.
		 * * De 50 a 99 alumnos, el costo es de 70 euros.
		 * * De 30 a 49 alumnos, el costo es de 95 euros.
		 * * Menos de 30 alumnos, el costo de la renta del autobús es de 4000 euros, sin importar el número de alumnos.
		 * Realiza un algoritmo que permita determinar el pago a la compañía de autobuses
		 * y lo que debe pagar cada alumno por el viaje.
		 */
		
		Scanner entrada = new Scanner(System.in);
		
		int alumnos;
		int costoAlumno;
		int pagoAutobus;
		
		System.out.println("Ingrese el número de alumnos que asisitrán:");
		alumnos = entrada.nextInt();
		
		if (alumnos>=100) {
			costoAlumno = 65;
			pagoAutobus = 65*alumnos;
		} else if (alumnos>=50 && alumnos<=99) {
			costoAlumno = 70;
			pagoAutobus = 70*alumnos;
		} else if (alumnos>=30 && alumnos<=49) {
			costoAlumno = 95;
			pagoAutobus = 95*alumnos;
		} else {
			pagoAutobus = 4000;
			costoAlumno = pagoAutobus/alumnos;
		}
		
		System.out.println("Deberá cobrar a cada alumno : €" + costoAlumno);
		System.out.println("Deberá pagar a la compañía de autobuses : €" + pagoAutobus);

	}

}
