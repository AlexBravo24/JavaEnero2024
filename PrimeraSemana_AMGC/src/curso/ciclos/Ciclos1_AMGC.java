package curso.ciclos;

public class Ciclos1_AMGC {

	public static void main(String[] args) {
		
		//1.- Programa un algoritmo que realice la tabla de multiplicar del 12
		
		int i=1;
		int resultado;
		
		while (i<=12) {
			resultado=i*12;
			System.out.println(i + " x 12 =" + resultado);
			i++;
		}

	}

}
