package com;

public class Alumno {
	private String nombre;
	private int edad; 
	private String escuela;
	private double altura;
	private double peso;
	
	
	
	public Alumno() {
		super();
	}



	public Alumno(String nombre, int edad, String escuela, double altura, double peso) {
		super();
		this.nombre = nombre;
		this.edad = edad;
		this.escuela = escuela;
		this.altura = altura;
		this.peso = peso;
	}
	
	
}
